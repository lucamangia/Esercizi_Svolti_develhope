package co.develhope.Lezioni.Lezione_26_11;

public class StringConcatenator {

    public String concat(String s1, String s2){
        return s1 +" " + s2;
    }
    public String concat(String s1, String s2,String s3){
        return s1 +" " + s2 + " " + s3;
    }
    public String concat(String s1, int n){
        return s1 + " " + n;
    }


}
